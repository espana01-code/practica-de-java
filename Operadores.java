public class Operadores {
    public static void main(String[] args) {

        // + (suma)

        // - (resta)

        // * (multiplicación)

        // / (división)

        // % (módulo: el residuo de una división)
        int a = 10;
        int b = 3;
        System.out.println("suma: " + (a + b));
        System.out.println("resta: " + (a - b));
        System.out.println("multipicacion: " + (a * b));
        System.out.println("división: " + (a / b));
        System.out.println("módulo: " + (a % b));

        int x = 20;
        int y = 23;
        System.out.println("El resultado de la suma es: " + (x + y));
        System.out.println("El resultado de la resta es: " + (x - y));
        System.out.println("El resultado de la multiplcacion es: " + (x * y));
        System.out.println("El resultado de la divicion es: " + (x / y));
        System.out.println("El resultado de la modulo es: " + (x % y));

    }

}
